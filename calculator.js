class calculator {
  static add(a, b) {
    let sum = a + b;
    return sum;
  }
  static sub(a, b) {
    let sub = a - b;
    return sub;
  }
  static multi(a, b) {
    let multi = a * b;
    return multi;
  }
  static div(a, b) {
    let div = a / b;
    return div;
  }
}
module.exports = calculator;
